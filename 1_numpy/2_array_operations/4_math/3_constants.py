import numpy as np

# using the constant pi
radius = 2
circumference = 2 * np.pi * radius
print(circumference)

# using the constant e
y = np.e
print(y)

# compute the exponential of a number
x = 1
y = np.exp(x)
print(y)

# add each array element with the constant pi
array1 = np.array([1, 2, 3])
y = array1 + np.pi
print(y)
